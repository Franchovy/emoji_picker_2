/// All the possible categories that [Emoji] can be put into
///
/// All [Category] are shown in the keyboard bottombar with the exception of [Category.Recommended]
/// which only displays when keywords are given
enum Category {
  Recommended,
  Recent,
  Smileys,
  Animals,
  Foods,
  Travel,
  Activities,
  Objects,
  Symbols,
  Flags
}
